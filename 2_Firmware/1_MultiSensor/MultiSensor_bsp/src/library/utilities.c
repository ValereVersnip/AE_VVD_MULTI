/**
 * Shift in a bit to the right of a 16bit value.
 * @param value_16bit 16bit value
 * @param bit true: shift in 1, false: shift in 0;
 * @return shifted in value
 */
uint16_t UTILITIES_ShiftInRightBit_16bit(uint16_t value_16bit, bool bit)
{
	if(bit)
	{
		return (value_16bit << 1) | 0x0001;
	}
	else
	{
		return (value_16bit << 1) & 0xFFFE;
	}
}




