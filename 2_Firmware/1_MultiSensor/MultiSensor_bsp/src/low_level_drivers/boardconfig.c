/**
 * Sets up system pin muxing prior to call to main.
 */
static void boardconfig_setupmuxing(void)
{
	/* Enable IOCON and Switch Matrix clocks */
	Chip_Clock_EnablePeriphClock(SYSCTL_CLOCK_IOCON);
}


/**
 * Set up and initialize clocking prior to call to main.
 */
static void boardconfig_setupclocking(void)
{
	/* Crystal is available on the board
	 * but not connected by default. Setting external crystal should be done after pinmuxing
	 */
	Chip_SetupIrcClocking();
}

/*
 * ***********************************************************************************************************************************************
 * Public Functions
 * ***********************************************************************************************************************************************
 */

/**
 * Set up and initialize hardware prior to call to main.
 *
 * This routine will initialize basic pinmuxing and clock configuration.
 */
void BOARDCONFIG_SystemInit(void)
{
	/* Setup system clocking and muxing */
	boardconfig_setupmuxing();
	boardconfig_setupclocking();

	/* IOCON clock left on, but may be turned off if no other IOCON
	   changes are needed */
}


