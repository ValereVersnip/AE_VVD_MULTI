int main(void) {

	status_t stat;

	/* Read clock settings and update SystemCoreClock variable */
	SystemCoreClockUpdate();

	/* Initialization of lowlevel drivers */
	stat = LOWLEVELDRIVERS_Init();
	if(stat != status_ok)
	{
		while(1)
		{
			stat = stat;		/* we should never come here.... */
		}
	}


	/* initialization of highlevel drivers */
	stat = HIGHLEVELDRIVERS_Init();
	USERAPP_HandleStatus(stat);





	/* initialize the user application */
	USERAPP_Init();


	/* neverending story */
    while(1)
    {
    	/* lowleveldrivers run0 function */
    	stat = LOWLEVELDRIVERS_Run0();
    	USERAPP_HandleStatus(stat);

    	/* highleveldrivers run0 function */
    	stat = HIGHLEVELDRIVERS_Run0();
    	USERAPP_HandleStatus(stat);

    	/* userapp run0 function */
    	USERAPP_Run0();


    }
    return 0 ;
}


